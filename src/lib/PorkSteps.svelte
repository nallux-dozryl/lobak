<ol class="list-decimal list-inside">
    <li>cut the lard into strips of 0.5cm thickness</li>
    <li>cut the pork into strips of 1cm thickness</li>
    <li>put the pork strips into a large bowl</li>
    <li>mix the spices, soy sauce, garlic, spring onion, sugar, and flour into the pork</li>
    <li>let it rest for about 10 minutes (optional)</li>
    <li>cut the beancurd sheets into 4 inch squares (use wet cloth to soften)</li>
    <li>put 3 pork strips and 1 lard strip on top of the beancurd sheets</li>
    <li>roll the beancurd sheet around the pork and lard</li>
    <li>seal with a little starch</li>
    <li>deep fry on medium heat until golden brown</li>
    <li>drain the oil and serve</li>
</ol>